            <strong>Baby & Me (ages 0-2.5)</strong> <br>
            Baby & Me classes are a fun way for care givers and their babies or
            young toddlers to connect with each other in a playful environment
            with other care givers and their babies. Moms get to shake up daily
            baby routines and interact with other care givers and their babies.
            They also get a chance to get some exercise, and experience first time
            interactions with other little ones. These classes are a fun learning
            environment for both care giver and child. Classes not only stimulate
            the learning process in the child, but nurture the existing bond between
            care giver and child in a way that allows the care giver to break away from the stresses of parenthood, while learning how to simultaneously play and grow with your child. Students may graduate to Twinkle Babies (dance) and/or Cheer/Acro Tumble after Baby & Me as soon as they are ready to separate and begin on their own and reach the required age for entry.
            <br>
            <br>
            <strong>Twinkle Babies (ages 2.5-3)</strong> <br>
            A thirty minute introductory dance class containing
            ballet, tap and creative movement. Young dancers
            will learn the basic steps while learning how to
            follow direction and imitate movement in a positive
            environment. Engaging music is used to get these
            little guys inspired to dance. The use of props such as Twinkle bears and hula hoops, enables the young dancers to use their imagination, creativity and, most of all, have fun!
            <br>
            <br>
            <strong>Twinkle Stars (ages 3-4, 4-5, 5-6)</strong> <br>
            An age specific 45 minute class containing ballet, tap and jazz/creative
            movement. The purpose of the class is to introduce young dancers to
            the three styles of dance and inspire movement and creativity within a
            positive environment. Dancers will also learn basic steps and
            terminology in all three styles. For example, all dancers will learn what
            a “plie” is and how to demonstrate one. The use of fun, age appropriate
            music engages the young dancers’ minds and bodies. The use of props
            such as hula hoops for the “freeze dance” makes the end of class the most fun! For Twinkle Stars 3 & 4, students must be 3.5 years old by August 1st (unless previously a Twinkle Baby).
            <br>
            <br>
            <strong>Star Track Classes (ages 2-6)</strong> <br>
            Classes offered for students that would like to take 2 or more classes a week.
            Students on the Star Track see more progress at a young age, and are
            developed at a young age a more versatile performer. Star Track Classes are
            recommended in addition to a Twinkle Stars class. Star Track classes are:
            <br>
            <em>Baby Bop (hip hop for ages 2-6)</em> <br>
            <em>Contemporary/Lyrical (expressive movement for ages 2-6)</em>
            <br>
            <br>
            <strong>Basic Ballet (ages 7 & up)</strong> <br>
            Classical technique is taught with an emphasis on posture and poise.
            Both barre and center work are taught in this class. Introductory class
            to Ballet. All students should take this class before advancing to
            Level I Ballet.
            <br>
            <br>
            <strong>Ballet I-IV & Pre Pointe'/Pointe' (ages 8 & up)</strong> <br>
            Classical technique is taught with an emphasis on posture and poise.
            Both barre and center work are learned in this class. Beginner through
            advanced levels are offered, with pointe work beginning at the
            advanced level. Ballet II-IV meet twice a week.
            <br>
            <br>
            <strong>Tap I-III (ages 7 & up)</strong> <br>
            Dancers will learn tap, which is a type of dance that combines timing, rhythm, and coordination. Tap classes are designed to develop rhythm, style and sound.
            <br>
            <br>
            <strong>Jazz I-III (ages 7 & up)</strong> <br>
            Jazz encompasses many styles of dance. Jazz uses the language
            of ballet, but puts the movement together in a different way. Jazz
            will focus on learning proper dance technique such as isolations of
            the body, improving performance quality and developing complex
            rhythms and patterns.
            <br>
            <br>
            <strong>Hip Hop Beginner, Intermediate, and Advance (ages 7 & up)</strong> <br>
            Hip hop is a high-energy class that infuses the latest styles of street dancing, breaking, popping, and locking. Classes will encourage students to step outside of the box by bringing their own individual style and personality to the movements. Hip Hop class provides a structured method of learning various Hip Hop dance movements in a fun-filled and vibrant class environment.
            <br>
            <br>
            <strong>Contemporary/Lyrical I & II (ages 7 & up)</strong> <br>
            A class that teaches modern contemporary technique and lyrical elements. Students will utilize balance, isolations, and connect movement to music.
            <br>
            <br>
            <strong>Flexibility & Strength Class (ages 7 & up)</strong> <br>
            A class designed to improve ones flexibility and increase strength. This
            class helps to create a overall better student & overall performer.
            <br>
            <br>
            <strong>Musical Theatre (ages 7 & up)</strong> <br>
            This class is a theatrical style of performing. It includes dancing, acting, and sometimes singing. Think Broadway style dancing!
            <br>
            <br>
            <strong>Cheer Tumble (ages 2-6) & Level I-IV (ages 6 & up)</strong> <br>
            Cheer classes encompass tumbling, stunting, & cheer prep
            classes. This class will focus on the basic fundamentals of
            cheerleading, such as motion technique, jumps, cheer dance,
            stunt technique and basic tumbling. The goal is to increase
            self- confidence, movement memory, technical skills, endurance,
            strength, flexibility, and teamwork.
            <br>
            <br>
            <strong>Acro Tumble (ages 2-6) & Level I-IV (ages 6 & up)</strong> <br>
            Acro Tumble classes are tumble technique classes. Cheer will not be taught in Acro Tumble. Recommended class for dancers. It is centered on fundamental acrobatic technique to increase flexibility, strength, balance, coordination, endurance, timing, body awareness, self-discipline, and confidence. Also referred to as floor gymnastics.
